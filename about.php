    <h1>Retinoblastoma.</h1>
    <h3>
      Retinoblastoma is a cancer that starts in the retina, the very back
      part of the eye. It is the most common type of eye cancer in
      children. It is a rare type of eye cancer that usually develops in
      early childhood, typically before the age of 5.
    </h3>
    <h3>
      How does retinoblastoma develop?
      <br />
      The eyes start to develop well before birth. During the early stages
      of development, the eyes have cells called retinoblasts, which
      multiply to make new cells that fill the retina. At a certain point,
      these cells stop multiplying and become mature retinal cells.
    </h3>
    <h3>
      Rarely, something goes wrong with this process. Instead of maturing,
      some retinoblasts continue to grow out of control, forming a cancer
      known as retinoblastoma.
    </h3>
    <h3>
      he chain of events inside cells that leads to retinoblastoma is
      complex, but it almost always starts with a change (mutation) in the
      RB1 gene. The normal RB1 gene helps keep cells from growing out of
      control, but a change in the gene stops it from working like it
      should. Depending on when and where the change in the RB1 gene
      occurs, it can result in 2 different types of retinoblastoma.
    </h3>
    <h3>
      Congenital (heritable) retinoblastoma In about 1 out of 3 children
      with retinoblastoma, the abnormality in the RB1 gene is congenital
      (present at birth) and is in all the cells of the body, including
      all of the cells of both retinas. This is known as a germline
      mutation.
    </h3>
    <h3>
      Despite this sometimes being called 'heritable' (or 'hereditary'),
      in most of these children, there is no family history of this
      cancer, and the RB1 gene change is not inherited from a parent. In
      these children, the gene change first occurs during early
      development in the womb. Only a small portion of the children born
      with this gene change inherit it from a parent.
    </h3>
    <h3>
      Children born with a mutation in the RB1 gene usually develop
      retinoblastoma in both eyes (known as bilateral retinoblastoma), and
      there are often several tumors within the eye (known as multifocal
      retinoblastoma). Because all of the cells in the body have the
      changed RB1 gene, these children also have a higher risk of
      developing cancers in other parts of the body.
    </h3>
    <h3>
      A small number of children with this form of retinoblastoma will
      develop another tumor in the brain, usually in the pineal gland at
      the base of the brain (a pineoblastoma). This is also known as
      trilateral retinoblastoma. For survivors of hereditary
      retinoblastoma, the risk of developing other cancers later in life
      is also higher than average (to learn more, see After Treatment for
      Retinoblastoma).
    </h3>
